PRInt32 sjiscontext_analysis_ngetorder(const char* str, PRUint32 *charlen) {

	/* Find out the current char's byte length */
	if((unsigned char)*str >= (unsigned char)0x81 &&
	   (unsigned char)*str <= (unsigned char)0x9f ||
	   (unsigned char)*str >= (unsigned char)0x0e &&
	   (unsigned char)*str <= (unsigned char)0xfc) {
		*charlen = 2;
	}
	else {
		*charlen = 1;
	}

	/* return its order if it is hiragana */
	if(*str == '\202' &&
	   (unsigned char)*(str+1) >= (unsigned char)0x9f &&
	   (unsigned char)*(str+1) <= (unsigned char)0xf1) {
		return (unsigned char)*(str+1) - (unsigned char)0x9f;
	}
	return -1;
}


PRInt32 sjiscontext_analysis_getorder(const char* str) {

	/* we are only interested in Hiragana, so the first byte is '\202' */
	if(*str == '\202' &&
	   (unsigned char)*(str+1) >= (unsigned char)0x9f &&
	   (unsigned char)*(str+1) <= (unsigned char)0xf1 )  {
		return (unsigned char)*(str+1) - (unsigned char)0x9f;
	}

	return -1;
}


