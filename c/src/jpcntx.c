int jpcontext_analysis_init(jpcontext_analysis* THIS) {
	return jpcontext_analysis_reset(THIS);
}


int jpcontext_analysis_reset(jpcontext_analysis* THIS) {
	PRUint32 i;
	THIS->totalrel = 0;
	for(i = 0; i < NUM_OF_CATEGORY; i++) {
		THIS->relsample[i] = 0;
	}
	THIS->needtoskipnum = 0;
	THIS->lastcharorder = -1;
	THIS->done = PR_FALSE;

	return 1;
}


int jpcontext_analysis_destroy(jpcontext_analysis* THIS) {
	return 1;
}


void jpcontext_analysis_handledata(jpcontext_analysis* THIS, const char* abuf,
                                   PRUint32 alen) {
	PRUint32 charlen;
	PRInt32  order;
	PRUint32 i;

printf("handldatacalled.\n");

	if(THIS->done) return;

	/* The buffer we got is byte orieted, and a character may span in more
	** than one buffer.  In case the last one or two bytes in the last buffer
	** is not complete, we record how many bytes are needed to complete that
	** character and skip thses bytes here.  We can choose to record those
	** bytes as well and analyze the character once it is complete, but since
	** a character will not make much difference, skipping this character will
	** simplify our logic and improve performance. */
	for(i = THIS->needtoskipnum; i < alen; ) {
		if(THIS->issjis) {
			order = sjiscontext_analysis_ngetorder(abuf + i, &charlen);
		}
		else {
			order = eucjpcontext_analysis_ngetorder(abuf + i, &charlen);
		}
		i += charlen;

		if(i > alen) {
			THIS->needtoskipnum = i - alen;
			THIS->lastcharorder = -1;
		}
		else {
			if(order != -1 && THIS->lastcharorder != -1) {
				THIS->totalrel++;
				if(THIS->totalrel > MAX_REL_THRESHOLD) {
					THIS->done = PR_TRUE;
					break;
				}
				THIS->relsample[jp2char_context[THIS->lastcharorder][order]]++;
			}
			THIS->lastcharorder = order;
		}
	}

	return;
}


void jpcontext_analysis_handleonechar(jpcontext_analysis* THIS,
                                      const char* astr, PRUint32 acharlen) {

	PRInt32 order;

	/* if we reveived enough data, stop here */
	if(THIS->totalrel > MAX_REL_THRESHOLD) THIS->done = PR_TRUE;
	if(THIS->done) return;

	/* Only 2-byte characters are of interest */
	if(order = (acharlen == 2)) {
		if(THIS->issjis == PR_TRUE) {
			sjiscontext_analysis_getorder(astr);
		}
		else {
			eucjpcontext_analysis_getorder(astr);
		}
	}
	else {
		order = -1;
	}

	if(order != -1 && THIS->lastcharorder != -1) {
		THIS->totalrel++;
		/* count this sequence to its category counter */
		THIS->relsample[jp2char_context[THIS->lastcharorder][order]]++;
	}

	THIS->lastcharorder = order;

	return;
}



float jpcontext_analysis_getconfidence(jpcontext_analysis* THIS) {

	/* This is just one way to calculate confidence.  It seems to work well */
	if(THIS->totalrel > MINIMUM_DATA_THRESHOLD) {
		return ((float)(THIS->totalrel - THIS->relsample[0]))/THIS->totalrel;
	}
	else {
		return (float)DONT_KNOW;
	}
}


PRBool jpcontext_analysis_gotenoughdata(jpcontext_analysis* THIS) {
	return THIS->totalrel > ENOUGH_REL_THRESHOLD;
}


