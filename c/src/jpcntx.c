PRInt32 eucjpcontext_analysis_ngetorder(const char* str, PRUint32 *charlen) {

	/* Find out the current char's byte length */
	if((unsigned char)*str == (unsigned char)0x8e ||
	   (unsigned char)*str >= (unsigned char)0xa1 &&
	   (unsigned char)*str <= (unsigned char)0xfe) {
		*charlen = 2;
	}
	else if((unsigned char)*str == (unsigned char)0x8f) {
		*charlen = 3;
	}
	else {
		*charlen = 1;
	}

	/* return its order if it is hiragana */
	if((unsigned char)*str == (unsigned char)0xa4 &&
	   (unsigned char)*(str+1) >= (unsigned char)0xa1 &&
	   (unsigned char)*(str+1) <= (unsigned char)0xfe) {
		return (unsigned char)*(str+1) - (unsigned char)0xa1;
	}

	return -1;
}

PRInt32 eucjpcontext_analysis_getorder(const char* str) {

    /* we are only interested in Hiragana, so the first byte is '\244' */
    if(*str == '\244' &&
       (unsigned char)*(str+1) >= (unsigned char)0xa1 &&
       (unsigned char)*(str+1) <= (unsigned char)0xf3 )  {
        return (unsigned char)*(str+1) - (unsigned char)0xa1;
    }

    return -1;
}
